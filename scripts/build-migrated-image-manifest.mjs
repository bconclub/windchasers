import { readdirSync, statSync, writeFileSync } from "node:fs";
import path from "node:path";

const ROOT = path.join(process.cwd(), "public", "migrated");
const OUT = path.join(process.cwd(), "content", "shared", "migratedImages.ts");

const BAD = /icon|logo|cropped-wc|favicon|whatsapp|call20|mail20|wtspp|placeholder|book-cover|zoom|windchasers-logo/i;
const RESIZED = /-\d{2,4}x\d{2,4}\.[a-z]+$/i; // skip -1024x683 style variants, keep originals
const IMG = /\.(webp|jpg|jpeg|png|avif)$/i;

// Prefer wide landscape-ish hero candidates by filename hints
function score(name) {
  let s = 0;
  if (/plane|aircraft|cockpit|sky|runway|airport|flight|flying|helicopter|cabin|pilot|hpl|dgca|skyline|city/i.test(name)) s += 5;
  if (/scaled|large|hero|main|banner/i.test(name)) s += 3;
  if (/\.png$/i.test(name)) s -= 1; // png often graphics
  return s;
}

const map = {};
let slugs = [];
try { slugs = readdirSync(ROOT).filter((d) => statSync(path.join(ROOT, d)).isDirectory()); } catch { slugs = []; }

for (const slug of slugs) {
  const dir = path.join(ROOT, slug);
  let files = [];
  try { files = readdirSync(dir); } catch { continue; }
  const usable = files
    .filter((f) => IMG.test(f) && !BAD.test(f) && !RESIZED.test(f))
    .sort((a, b) => score(b) - score(a) || a.localeCompare(b))
    .map((f) => `/migrated/${slug}/${f.split("/").map(encodeURIComponent).join("/")}`);
  if (usable.length) map[slug] = usable.slice(0, 8);
}

const body =
  "// AUTO-GENERATED by scripts/build-migrated-image-manifest.mjs — do not edit by hand.\n" +
  "// Maps each migrated page slug to its usable full-size photos (best hero first).\n\n" +
  "export const migratedImages: Record<string, string[]> = " +
  JSON.stringify(map, null, 2) +
  ";\n";

writeFileSync(OUT, body);
const total = Object.values(map).reduce((n, a) => n + a.length, 0);
console.log(`slugs with images: ${Object.keys(map).length}, total images: ${total}`);
console.log(`wrote ${OUT}`);
