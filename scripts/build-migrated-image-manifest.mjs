import { readdirSync, statSync, writeFileSync } from "node:fs";
import path from "node:path";

const ROOT = path.join(process.cwd(), "public", "migrated");
const OUT = path.join(process.cwd(), "content", "shared", "migratedImages.ts");

// PNGs in the WordPress export are icon/graphic assets (elementor line icons,
// flags, aeroplane/passport glyphs). Real photographs are webp/jpg/avif only.
const PHOTO = /\.(webp|jpg|jpeg|avif)$/i;
const BAD = /icon|logo|cropped-wc|favicon|whatsapp|call20|mail20|wtspp|placeholder|book-cover|zoom|windchasers-logo|flag|illustration|clipart/i;
const RESIZED = /-\d{2,4}x\d{2,4}\.[a-z]+$/i; // skip -1024x683 variants, keep originals

function score(name) {
  let s = 0;
  if (/plane|aircraft|cockpit|sky|runway|airport|flight|flying|helicopter|cabin|pilot|hpl|dgca|skyline|city|gallery/i.test(name)) s += 5;
  if (/scaled|large|hero|main|banner/i.test(name)) s += 3;
  return s;
}

const map = {};
let slugs = [];
try {
  slugs = readdirSync(ROOT).filter((d) => statSync(path.join(ROOT, d)).isDirectory());
} catch {
  slugs = [];
}

for (const slug of slugs) {
  const dir = path.join(ROOT, slug);
  let files = [];
  try {
    files = readdirSync(dir);
  } catch {
    continue;
  }
  const usable = files
    .filter((f) => PHOTO.test(f) && !BAD.test(f) && !RESIZED.test(f))
    .sort((a, b) => score(b) - score(a) || a.localeCompare(b))
    .map((f) => "/migrated/" + slug + "/" + encodeURIComponent(f));
  if (usable.length) map[slug] = usable.slice(0, 8);
}

const body =
  "// AUTO-GENERATED by scripts/build-migrated-image-manifest.mjs - do not edit by hand.\n" +
  "// Maps each migrated page slug to its usable full-size PHOTOS (webp/jpg/avif only, no icon PNGs).\n\n" +
  "export const migratedImages: Record<string, string[]> = " +
  JSON.stringify(map, null, 2) +
  ";\n";

writeFileSync(OUT, body);
const total = Object.values(map).reduce((n, a) => n + a.length, 0);
console.log("slugs with images: " + Object.keys(map).length + ", total photos: " + total);
